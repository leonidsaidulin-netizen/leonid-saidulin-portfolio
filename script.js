const languageButton = document.querySelector(".language-toggle");
const resumeButton = document.querySelector(".resume-download");
const metaDescription = document.querySelector('meta[name="description"]');

document.querySelectorAll("[data-i18n]").forEach((node) => {
  node.dataset.ru = node.textContent;
});

function setLanguage(language) {
  const lang = language === "en" ? "en" : "ru";
  document.documentElement.lang = lang;
  document.title = lang === "ru" ? "Леонид Сайдулин — Product Lead" : "Leonid Saidulin — Product Lead";
  metaDescription.content = lang === "ru"
    ? "Леонид Сайдулин — Product Lead. SaaS, PropTech, биллинг, голосовые роботы и AI-автоматизация."
    : "Leonid Saidulin — Product Lead. SaaS, PropTech, utility billing, voice AI, and AI-enabled product operations.";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = lang === "en" && en[key] ? en[key] : node.dataset.ru;
  });

  document.querySelectorAll("[data-language]").forEach((node) => {
    node.classList.toggle("active", node.dataset.language === lang);
  });

  if (resumeButton) {
    resumeButton.href = lang === "ru"
      ? "assets/leonid_saidulin_product_resume_ru.docx"
      : "assets/leonid_saidulin_product_resume_en.docx";
  }
  localStorage.setItem("portfolio-language", lang);
}

languageButton.addEventListener("click", () => {
  setLanguage(document.documentElement.lang === "ru" ? "en" : "ru");
});

const preferredLanguage = localStorage.getItem("portfolio-language")
  || (navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en");
setLanguage(preferredLanguage);

const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const revealItems = document.querySelectorAll(".reveal");
if (reducedMotion || !("IntersectionObserver" in window)) {
  revealItems.forEach((item) => item.classList.add("visible"));
} else {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });
  revealItems.forEach((item) => observer.observe(item));
}

const progress = document.querySelector(".scroll-progress span");
function updateProgress() {
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const ratio = scrollable > 0 ? window.scrollY / scrollable : 0;
  progress.style.width = `${Math.min(100, Math.max(0, ratio * 100))}%`;
}
window.addEventListener("scroll", updateProgress, { passive: true });
window.addEventListener("resize", updateProgress);
updateProgress();

